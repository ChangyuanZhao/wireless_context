"""
DQN Training Script for Modality Selection

This script trains a DQN agent to select the optimal modality combination
at each time step to maximize cumulative reward (top-3 accuracy) over sequences.

State: cls_output from previous time step (embed_dim=512)
Action: Modality combination (0: only_pos, 1: missing_lidar, 2: missing_image, 3: full_observation)
Reward: accuracy_reward - lambda * modality_cost
        where accuracy_reward = 1 if top-3 correct, 0 otherwise
        and modality_cost is based on encoder FLOPs (pos=0.01, lidar=0.1, image=0.9)

Usage:
    python dqn_train.py --checkpoint ./checkpoints/best_model.pth

Author: Generated by Claude
"""

