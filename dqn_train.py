# Transition for replay buffer
Transition = namedtuple('Transition', ('state', 'action', 'reward', 'next_state', 'done'))

# Modality configurations (4 actions) - matches train.py
# Relative computation cost based on encoder FLOPs:
#   - Position encoder: ~0.5M FLOPs  -> cost = 0.01 (negligible)
#   - LiDAR encoder:    ~20M FLOPs   -> cost = 0.1
#   - Image encoder:    ~2.1G FLOPs  -> cost = 0.9
MODALITY_CONFIGS = [
    {'name': 'only_pos', 'use_pos': True, 'use_lidar': False, 'use_image': False, 'cost': 0.01},
    {'name': 'missing_lidar', 'use_pos': True, 'use_lidar': False, 'use_image': True, 'cost': 0.91},
    {'name': 'missing_image', 'use_pos': True, 'use_lidar': True, 'use_image': False, 'cost': 0.11},
    {'name': 'full_observation', 'use_pos': True, 'use_lidar': True, 'use_image': True, 'cost': 1.0},
]
NUM_ACTIONS = len(MODALITY_CONFIGS)


class DQN(nn.Module):
    """
    Deep Q-Network for modality selection.

    Input: cls_output (embed_dim=512)
    Output: Q-values for 4 actions (modality combinations)
    """

    def __init__(self, state_dim=512, num_actions=4, hidden_dim=256):
        super().__init__()
        self.network = nn.Sequential(
            nn.Linear(state_dim, hidden_dim),
            nn.ReLU(),
            nn.Linear(hidden_dim, hidden_dim),
            nn.ReLU(),
            nn.Linear(hidden_dim, num_actions),
        )

    def forward(self, state):
        return self.network(state)


class ReplayBuffer:
    """Experience replay buffer for DQN."""

    def __init__(self, capacity=100000):
        self.buffer = deque(maxlen=capacity)

    def push(self, *args):
        self.buffer.append(Transition(*args))

    def sample(self, batch_size):
        transitions = random.sample(self.buffer, batch_size)
        batch = Transition(*zip(*transitions))
        return batch

    def __len__(self):
        return len(self.buffer)


class ModalityEnv:
    """
    Environment for modality selection using a pre-trained BeamPredictionModel.

    For each sequence, the agent selects modalities at each time step.
    State is the cls_output from the previous prediction.
    Reward is 1 if the prediction is in top-3, 0 otherwise.
    """

    def __init__(self, beam_model, device):
        self.beam_model = beam_model
        self.device = device
        self.embed_dim = beam_model.embed_dim

        # Initial state (learnable or zero)
        self.initial_state = torch.zeros(1, self.embed_dim, device=device)

    @torch.no_grad()
    def get_cls_output(self, gps, lidar, image, action):
        """
        Get cls_output for a given modality configuration.

        Returns:
            cls_output: (1, embed_dim)
            logits: (1, num_classes)
        """
        config = MODALITY_CONFIGS[action]

        # Apply modality mask
        pos = gps if config['use_pos'] else None
        lidar_input = lidar if config['use_lidar'] else None
        image_input = image if config['use_image'] else None

        # Get batch size and device
        batch_size, device = self.beam_model._get_batch_size_and_device(pos, lidar_input, image_input)

        # Prepare CLS token
        cls_tokens = self.beam_model.cls_token.expand(batch_size, -1, -1) + self.beam_model.cls_type_embed

        # Encode each modality
        pos_tokens = self.beam_model.encode_position(pos, batch_size, device)
        lidar_tokens = self.beam_model.encode_lidar(lidar_input, batch_size, device)
        image_tokens = self.beam_model.encode_image(image_input, batch_size, device)

        # Concatenate
        tokens = torch.cat([cls_tokens, pos_tokens, lidar_tokens, image_tokens], dim=1)

        # Pass through transformer
        output = self.beam_model.transformer(tokens)

        # Extract CLS output
        cls_output = output[:, 0, :]  # (B, embed_dim)

        # Get logits
        logits = self.beam_model.classifier(cls_output)

        return cls_output, logits

    def compute_reward(self, logits, label, action, cost_lambda=0.1, top_k=3):
        """
        Compute reward based on top-k accuracy minus modality cost.

        Reward = accuracy_reward - cost_lambda * modality_cost

        Args:
            logits: (1, num_classes)
            label: int
            action: int (0-3)
            cost_lambda: float, weight for cost penalty
            top_k: int

        Returns:
            reward: float
            accuracy_reward: float (1.0 if correct in top-k, 0.0 otherwise)
        """
        _, pred_topk = logits.topk(top_k, dim=1)
        correct = (pred_topk == label).any().item()
        accuracy_reward = 1.0 if correct else 0.0

        # Modality cost penalty
        modality_cost = MODALITY_CONFIGS[action]['cost']
        reward = accuracy_reward - cost_lambda * modality_cost

        return reward, accuracy_reward


