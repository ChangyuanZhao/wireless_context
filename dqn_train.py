def get_epsilon(episode, epsilon_start, epsilon_end, epsilon_decay):
    """Compute epsilon for epsilon-greedy policy."""
    return epsilon_end + (epsilon_start - epsilon_end) * np.exp(-episode / epsilon_decay)


def select_action(state, policy_net, epsilon, device):
    """Select action using epsilon-greedy policy."""
    if random.random() < epsilon:
        return random.randrange(NUM_ACTIONS)
    else:
        with torch.no_grad():
            q_values = policy_net(state)
            return q_values.argmax(dim=1).item()


class FixedPolicy(nn.Module):
    """Policy that always selects a fixed action."""
    def __init__(self, action):
        super().__init__()
        self.action = action

    def forward(self, state):
        return torch.zeros(state.size(0), NUM_ACTIONS, device=state.device).scatter_(
            1, torch.full((state.size(0), 1), self.action, device=state.device), 1.0
        )


class RandomPolicy(nn.Module):
    """Policy that selects actions uniformly at random."""
    def __init__(self):
        super().__init__()

    def forward(self, state):
        # Return uniform Q-values so argmax picks randomly
        return torch.rand(state.size(0), NUM_ACTIONS, device=state.device)


def optimize_model(policy_net, target_net, optimizer, replay_buffer, batch_size, gamma, device):
    """Perform one step of DQN optimization."""
    if len(replay_buffer) < batch_size:
        return None

    # Sample batch
    batch = replay_buffer.sample(batch_size)

    # Convert to tensors
    state_batch = torch.cat(batch.state)
    action_batch = torch.tensor(batch.action, device=device).unsqueeze(1)
    reward_batch = torch.tensor(batch.reward, device=device, dtype=torch.float32)
    next_state_batch = torch.cat(batch.next_state)
    done_batch = torch.tensor(batch.done, device=device, dtype=torch.float32)

    # Compute Q(s, a)
    q_values = policy_net(state_batch).gather(1, action_batch).squeeze(1)

    # Compute V(s') using target network
    with torch.no_grad():
        next_q_values = target_net(next_state_batch).max(1)[0]
        expected_q_values = reward_batch + gamma * next_q_values * (1 - done_batch)

    # Compute loss
    loss = F.smooth_l1_loss(q_values, expected_q_values)

    # Optimize
    optimizer.zero_grad()
    loss.backward()
    torch.nn.utils.clip_grad_norm_(policy_net.parameters(), 1.0)
    optimizer.step()

    return loss.item()


def run_episode(env, dataset, seq_indices, policy_net, epsilon, device,
                replay_buffer=None, training=True, cost_lambda=0.1, seq_idx=None):
    """
    Run one episode (one sequence) and collect transitions.

    Args:
        seq_idx: If provided, use this specific sequence. Otherwise randomly select.

    Returns:
        total_reward: float (with cost penalty)
        total_accuracy: float (pure top-k accuracy count)
        episode_length: int
        action_counts: dict
        total_cost: float
    """
    # Use specified sequence or randomly select
    if seq_idx is None:
        seq_idx = random.choice(seq_indices)

    # Get all windows for this sequence
    seq_windows = [(i, w) for i, w in enumerate(dataset.windows) if w[0] == seq_idx]

    if len(seq_windows) < 2:
        return 0.0, 0.0, 0, {}, 0.0

    # Sort by window indices to maintain temporal order
    seq_windows.sort(key=lambda x: x[1][1][0])  # Sort by first index in window

    total_reward = 0.0
    total_accuracy = 0.0
    total_cost = 0.0
    action_counts = {i: 0 for i in range(NUM_ACTIONS)}

    # Initial state
    state = env.initial_state.clone()

    for i, (dataset_idx, window_info) in enumerate(seq_windows):
        # Load data for this window
        sample = dataset[dataset_idx]
        gps = sample['gps'].unsqueeze(0).to(device)      # (1, 4, 2)
        lidar = sample['lidar'].unsqueeze(0).to(device)  # (1, 4, 216)
        lidar = lidar.unsqueeze(1)                        # (1, 1, 4, 216)
        image = sample['image'].unsqueeze(0).to(device)  # (1, 3, 540, 960)
        label = sample['label']

        # Select action
        action = select_action(state, policy_net, epsilon if training else 0.0, device)
        action_counts[action] += 1

        # Execute action and get next state
        next_state, logits = env.get_cls_output(gps, lidar, image, action)

        # Compute reward (with cost penalty)
        reward, accuracy_reward = env.compute_reward(logits, label, action, cost_lambda, top_k=3)
        total_reward += reward
        total_accuracy += accuracy_reward
        total_cost += MODALITY_CONFIGS[action]['cost']

        # Check if done (last step in sequence)
        done = (i == len(seq_windows) - 1)

        # Store transition
        if replay_buffer is not None and training:
            replay_buffer.push(state, action, reward, next_state, done)

        # Update state
        state = next_state

    return total_reward, total_accuracy, len(seq_windows), action_counts, total_cost


def evaluate(env, dataset, seq_indices, policy_net, device, cost_lambda=0.1):
    """
    Evaluate the policy on ALL test sequences (deterministic).

    Iterates through all sequences instead of random sampling to ensure
    consistent results for deterministic policies.
    """
    total_rewards = []
    total_accuracies = []
    total_lengths = []
    total_costs = []
    all_action_counts = {i: 0 for i in range(NUM_ACTIONS)}

    # Iterate through ALL sequences
    for seq_idx in seq_indices:
        reward, accuracy, length, action_counts, cost = run_episode(
            env, dataset, seq_indices, policy_net, 0.0, device,
            replay_buffer=None, training=False, cost_lambda=cost_lambda,
            seq_idx=seq_idx  # Use specific sequence
        )
        if length == 0:
            continue
        total_rewards.append(reward)
        total_accuracies.append(accuracy)
        total_lengths.append(length)
        total_costs.append(cost)
        for k, v in action_counts.items():
            all_action_counts[k] += v

    avg_reward = np.mean(total_rewards) if total_rewards else 0.0
    avg_accuracy = np.mean(total_accuracies) if total_accuracies else 0.0
    avg_length = np.mean(total_lengths) if total_lengths else 0.0
    avg_cost = np.mean(total_costs) if total_costs else 0.0
    avg_acc_per_step = avg_accuracy / avg_length if avg_length > 0 else 0
    avg_cost_per_step = avg_cost / avg_length if avg_length > 0 else 0

    return avg_reward, avg_acc_per_step, avg_cost_per_step, all_action_counts


