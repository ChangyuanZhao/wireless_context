def compute_accuracy(logits, labels, topk=(1, 3, 5)):
    """
    Compute top-k accuracy.

    Args:
        logits: (B, num_classes)
        labels: (B,)
        topk: tuple of k values

    Returns:
        dict of accuracies
    """
    maxk = max(topk)
    batch_size = labels.size(0)

    _, pred = logits.topk(maxk, dim=1, largest=True, sorted=True)
    pred = pred.t()  # (maxk, B)
    correct = pred.eq(labels.view(1, -1).expand_as(pred))  # (maxk, B)

    results = {}
    for k in topk:
        correct_k = correct[:k].reshape(-1).float().sum(0)
        results[f'top{k}'] = correct_k.item() / batch_size * 100

    return results


def train_one_epoch(model, dataloader, criterion, optimizer, device, epoch, log_interval):
    """Train for one epoch with random modality dropout."""
    model.train()

    total_loss = 0
    total_correct = 0
    total_samples = 0
    config_counts = {k: 0 for k in MODALITY_CONFIGS.keys()}

    start_time = time.time()
    config_names = list(MODALITY_CONFIGS.keys())

    for batch_idx, batch in enumerate(dataloader):
        # Move data to device
        gps = batch['gps'].to(device)           # (B, 4, 2)
        lidar = batch['lidar'].to(device)       # (B, 4, 216)
        image = batch['image'].to(device)       # (B, 3, 540, 960)
        labels = batch['label'].to(device)      # (B,)

        # Reshape lidar: (B, 4, 216) -> (B, 1, 4, 216)
        lidar = lidar.unsqueeze(1)

        # Random modality dropout: select one of the 3 configurations
        config_name = random.choice(config_names)
        config = MODALITY_CONFIGS[config_name]
        config_counts[config_name] += 1

        # Apply modality dropout
        pos, lidar_input, image_input = apply_modality_dropout(gps, lidar, image, config)

        # Forward pass
        optimizer.zero_grad()
        logits = model(pos=pos, lidar=lidar_input, image=image_input)

        # Compute loss
        loss = criterion(logits, labels)

        # Backward pass
        loss.backward()
        optimizer.step()

        # Statistics
        total_loss += loss.item() * labels.size(0)
        pred = logits.argmax(dim=1)
        total_correct += (pred == labels).sum().item()
        total_samples += labels.size(0)

        # Logging
        if (batch_idx + 1) % log_interval == 0:
            elapsed = time.time() - start_time
            print(f'  Epoch {epoch} | Batch {batch_idx + 1}/{len(dataloader)} | '
                  f'Loss: {loss.item():.4f} | '
                  f'Acc: {total_correct / total_samples * 100:.2f}% | '
                  f'Time: {elapsed:.1f}s')

    avg_loss = total_loss / total_samples
    avg_acc = total_correct / total_samples * 100

    # Print modality dropout statistics
    print(f'  Modality usage: ' + ', '.join([f'{k}: {v}' for k, v in config_counts.items()]))

    return avg_loss, avg_acc


@torch.no_grad()
def evaluate_with_config(model, dataloader, criterion, device, config):
    """Evaluate on validation/test set with a fixed modality configuration."""
    model.eval()

    total_loss = 0
    total_samples = 0
    all_logits = []
    all_labels = []

    for batch in dataloader:
        # Move data to device
        gps = batch['gps'].to(device)
        lidar = batch['lidar'].to(device)
        image = batch['image'].to(device)
        labels = batch['label'].to(device)

        # Reshape lidar: (B, 4, 216) -> (B, 1, 4, 216)
        lidar = lidar.unsqueeze(1)

        # Apply modality dropout for this evaluation config
        pos, lidar_input, image_input = apply_modality_dropout(gps, lidar, image, config)

        # Forward pass
        logits = model(pos=pos, lidar=lidar_input, image=image_input)

        # Compute loss
        loss = criterion(logits, labels)

        total_loss += loss.item() * labels.size(0)
        total_samples += labels.size(0)

        all_logits.append(logits)
        all_labels.append(labels)

    # Concatenate all predictions
    all_logits = torch.cat(all_logits, dim=0)
    all_labels = torch.cat(all_labels, dim=0)

    # Compute metrics
    avg_loss = total_loss / total_samples
    accuracies = compute_accuracy(all_logits, all_labels, topk=(1, 3, 5))

    return avg_loss, accuracies


@torch.no_grad()
def evaluate_all_configs(model, dataloader, criterion, device):
    """Evaluate on validation/test set for all modality configurations."""
    results = {}
    top1_values = []

    for config_name, config in MODALITY_CONFIGS.items():
        loss, accs = evaluate_with_config(model, dataloader, criterion, device, config)
        results[config_name] = {'loss': loss, 'accs': accs}
        top1_values.append(accs['top1'])

    avg_top1 = sum(top1_values) / len(top1_values) if top1_values else 0.0
    return results, avg_top1


