# Modality configurations
MODALITY_CONFIGS = {
    'only_pos': {'use_pos': True, 'use_lidar': False, 'use_image': False},
    'missing_lidar': {'use_pos': True, 'use_lidar': False, 'use_image': True},
    'missing_image': {'use_pos': True, 'use_lidar': True, 'use_image': False},
    'full_observation': {'use_pos': True, 'use_lidar': True, 'use_image': True},
}


def apply_modality_dropout(gps, lidar, image, config):
    """
    Apply modality dropout based on configuration.

    Args:
        gps: (B, 4, 2) GPS data
        lidar: (B, 1, 4, 216) LiDAR data
        image: (B, 3, 540, 960) Image data
        config: dict with 'use_pos', 'use_lidar', 'use_image' keys

    Returns:
        pos, lidar, image (with None for dropped modalities)
    """
    pos_out = gps if config['use_pos'] else None
    lidar_out = lidar if config['use_lidar'] else None
    image_out = image if config['use_image'] else None
    return pos_out, lidar_out, image_out


